-- Public storefront read access
 drop policy if exists "Public read active products" on public.products;
create policy "Public read active products" on public.products for select to anon, authenticated using (active = true);
drop policy if exists "Public read active offers" on public.offers;
create policy "Public read active offers" on public.offers for select to anon, authenticated using (active = true);
drop policy if exists "Public read site settings" on public.site_settings;
create policy "Public read site settings" on public.site_settings for select to anon, authenticated using (true);

-- Authenticated admin access
 drop policy if exists "Authenticated manage products" on public.products;
create policy "Authenticated manage products" on public.products for all to authenticated using (true) with check (true);
drop policy if exists "Authenticated manage offers" on public.offers;
create policy "Authenticated manage offers" on public.offers for all to authenticated using (true) with check (true);
drop policy if exists "Authenticated manage settings" on public.site_settings;
create policy "Authenticated manage settings" on public.site_settings for all to authenticated using (true) with check (true);

-- Reviews
 drop policy if exists "Public read approved reviews" on public.reviews;
create policy "Public read approved reviews" on public.reviews for select to anon, authenticated using (approved = true);
drop policy if exists "Public submit reviews" on public.reviews;
create policy "Public submit reviews" on public.reviews for insert to anon, authenticated with check (approved = false);
drop policy if exists "Authenticated manage reviews" on public.reviews;
create policy "Authenticated manage reviews" on public.reviews for all to authenticated using (true) with check (true);

