create table if not exists public.products (
  id bigint generated by default as identity primary key,
  name text not null,
  design_number text,
  category text,
  description text,
  price numeric not null default 0,
  original_price numeric,
  image_url text,
  image_urls jsonb not null default '[]'::jsonb,
  sizes text,
  colors text,
  stock integer not null default 0,
  featured boolean not null default false,
  active boolean not null default true,
  created_at timestamptz not null default now()
);
alter table public.products add column if not exists image_urls jsonb default '[]'::jsonb;

create table if not exists public.offers (
  id bigint generated by default as identity primary key,
  title text not null default 'Special Offer',
  subtitle text,
  discount_text text,
  banner_image text,
  start_date date,
  end_date date,
  active boolean not null default true,
  created_at timestamptz not null default now()
);
alter table public.offers add column if not exists banner_image text;

create table if not exists public.site_settings (
  id bigint generated by default as identity primary key,
  store_name text default 'Tanisha Ethnics',
  whatsapp_number text default '918141152565',
  instagram_url text default 'https://www.instagram.com/tanisha.ethnic/',
  announcement_text text default 'Welcome to Tanisha Ethnics'
);

create table if not exists public.reviews (
  id bigint generated by default as identity primary key,
  customer_name text not null check (char_length(customer_name) between 1 and 60),
  rating integer not null check (rating between 1 and 5),
  review_text text not null check (char_length(review_text) between 2 and 600),
  approved boolean not null default false,
  created_at timestamptz not null default now()
);

